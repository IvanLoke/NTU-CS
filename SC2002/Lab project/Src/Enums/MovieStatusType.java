package Enums;

/**
 * Different Movie Statuses.
 * {@link #COMING_SOON}
 * {@link #PREVIEW}
 * {@link #NOW_SHOWING}
 * {@link #END_OF_SHOWING}
 * @author Chay Hui Xiang
 * @version 1.0
 * @since 2022-11-11
 */
public enum MovieStatusType {

    /**
     * Movies that are Coming Soon.
     */
    COMING_SOON,

    /**
     * Movies that are on Preview.
     */
    PREVIEW,

    /**
     * Movies that are currently showing.
     */
    NOW_SHOWING,

    /**
     * Movies that have ceased showing
     */
    END_OF_SHOWING
}
